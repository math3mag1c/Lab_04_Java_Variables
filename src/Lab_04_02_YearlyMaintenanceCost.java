public class Lab_04_02_YearlyMaintenanceCost {
    public static void main(String[] args) {
        double springCost = 120.24;
        double summerCost = 3458.35;
        double fallCost = 9874.25;
        double winterCost = 398476.35;
        double totalYearlyCost = springCost + summerCost + fallCost + winterCost;
        System.out.println("The spring cost is " + springCost);
        System.out.println("The summer cost is " + summerCost);
        System.out.println("The fall cost is " + fallCost);
        System.out.println("The winter cost is " + winterCost);
        System.out.println("The total yearly cost is " + totalYearlyCost + ".");
    }
}
